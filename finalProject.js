 // Get the modal
var modal = document.getElementById("myModal");

// Get the image and insert it inside the modal - use its "alt" text as a caption
var img = document.getElementById("myImg");
var modalImg = document.getElementById("img01");
var captionText = document.getElementById("caption");
img.onclick = function(){
    modal.style.display = "block";
    modalImg.src = this.src;
    modalImg.style.float = "left";
    modalImg.style.width = "40%";
    modalImg.style.marginLeft = "5%";
    modalImg.style.marginRight = "5%";
    captionText.innerHTML = document.getElementById("description").innerHTML;
    captionText.style.fontSize = "18px";
    captionText.style.textAlign = "left";
    captionText.style.marginTop = "-20px";
}

// Get the <span> element that closes the modal
var span = document.getElementsByClassName("close")[0];

// When the user clicks on <span> (x), close the modal
span.onclick = function() { 
    modal.style.display = "none";
}

